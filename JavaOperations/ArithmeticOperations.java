//Java Arithmetic Operations Program
public class ArithmeticOperations{
	public static void main(String[] args){
		int number1 = 25, number2 = 10;
		int sum = number1 + number2;
		int sub = number1 - number2;
		int mul = number1 * number2;
		int div = number1 / number2;
		int mode = number1 % number2;
		int squ = number1 * number1;
		
		System.out.println("Addition of two number : " +sum);
		System.out.println("Subtraction of two number : " +sub);
		System.out.println("Multiplication of two number : " +mul);
		System.out.println("Devision of two number : " +div);
		System.out.println("Mode of two number : " +mode);
		System.out.println("Square of two number : " +squ);
	}
}
