// Java Assignmnet Operations
// assignment ----> = += -= *= /= %= &= ^= |= <<= >>= >>>=

public class AssignmentOperations{
    public static void main(String args[]){
        int number1 = 20;
        int number2 = 25;
        System.out.println(number1 = number2);
        System.out.println(number1 += number2);
        System.out.println(number1 -= number2);
        System.out.println(number1 *= number2);
        System.out.println(number1 /= number2);
        System.out.println(number1 %= number2);
        System.out.println(number1 &= number2);
        System.out.println(number1 ^= number2);
        System.out.println(number1 |= number2);
        System.out.println(number1 <<= number2);
        System.out.println(number1 >>= number2);
        System.out.println(number1 >>>= number2);
        
    }
}