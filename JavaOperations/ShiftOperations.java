//Java Shift Operation Programs
/* 1 = 0001 and 2 = 0010*/
public class ShiftOperations{  
	public static void main(String args[]){  
		int number1 = 20; 
		int number2 = 3;
		System.out.println("Left Shift Operation Result : ");
		System.out.println(number1<<number2);
		System.out.println(" \n Right Shift Operation Result : ");
		System.out.println(number1>>number2);
		System.out.println("\n Left Shift Operation Result : ");
		System.out.println(number1>>>number2);
	}
}  
