//Java Unary Operations Program

public class UnaryOperations{
	public static void main(String[] args){
		int number1 = 18, number2 = 10 ;
		boolean number3 = false;
		
		System.out.println("Result1  : " + number1++); 
		System.out.println("Result2  : " + number2--);
		System.out.println("Result3  : " + ++number1);
		System.out.println("Result4  : " + --number2);
		System.out.println("Result5  : " + ~number1);	
		System.out.println("Result6  : " + !number3);
	}
}	
	
