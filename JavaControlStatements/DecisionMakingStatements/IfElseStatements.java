// Java If-Else statement

public class IfElseStatements{
    public static void main(String args[]){
        int number1 = 18;
        if(number1 >= 18){
            System.out.println("Candidate can get his/her driving license");
        }
        else{
            System.out.println("Candidate is not eligible to get driving license");
        }
    }
}